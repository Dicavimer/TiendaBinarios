    public Vector clientes (Usuario u){
        Vector v = new Vector();
        v.add(u.getUsuario());
        v.add(u.getNombre());
        v.add(u.getApellido());
        v.add(u.getEmail());
        v.add(u.getContrasena());
        v.add(u.getNumeroTelefonico());
        v.add(u.getNumeroIdentificacion());
        v.add(u.getTipo_Usuario());
        return v;
                
    }

